package javaclass;

public class _7Elseif {

	public static void main(String[] args) {
		char grade='Z';
		
		
		if(grade =='A') {
			System.out.println("your grade is very good");
		}
		else if(grade=='B') {
			
			System.out.println("keep learning");
			
		}
		else if (grade=='C') {
			System.out.println("keep improving");
		}
		else {
			System.out.println("invalid grade");
		}
	}

}
