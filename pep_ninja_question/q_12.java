package pep_ninja_question;

import java.util.Scanner;

public class q_12 {

	public static void main(String[] args) {
		// sum of even or odd 
		Scanner SC=new Scanner(System.in);
		int n=SC.nextInt();
	int sum=0;
	
		for(int i=1;i<=n;i++) {
	    	if(i%2==0)   // i%2!=0 odd
	    	{    		
	    		sum=sum+i;
   		
	    			
	  System.out.println(sum);
		    	
	    	}
	
	}
		
	
	
	}}
		
		
	


